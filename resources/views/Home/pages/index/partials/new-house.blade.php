        $(function () {
            $('.new-house-ol li').hover(function () {
                var that = $(this),
                    index = that.index();
                that.addClass('active').siblings().removeClass('active');
                if(index == 1){
                    $('.new-house-box').find('div.new-house-item').stop().animate({
                        left:'-100%',
                    });
                }
                if(index == 0){
                    $('.new-house-box').find('div.new-house-item').stop().animate({
                        left:'0',
                    });
                }
            })

            // 自动播
            function move(){
                i++;
                $('.new-house-ol li').eq(i).addClass('active').siblings().removeClass('active');

                if(i == 0){
                    $('.new-house-box').find('div.new-house-item').stop().animate({
                        left:'0',
                    });
                }
                if(i==1){
                    $('.new-house-box').find('div.new-house-item').stop().animate({
                        left:'-100%',
                    });
                    i=-1;
                }
            }
            var timer = null;
            var i = 0;
            timer = setInterval(move,4000);
            $('.box').mouseover(function(){
                clearInterval(timer);
            });
            $('.box').mouseout(function(){
                timer = setInterval(move,4000);
            })
        })
    </script>
